        <h3 style="margin-left:200px;">Cara Pembuatan Blackforest :</h3>
        <ol style="margin-left:200px;" align="justify">
          <li> Untuk membuat adonan kue, silahkan siapkan baskom dekat dengan jangkauan anda dan masukkan tepung terigu bersama dengan tepung mazeina dan juga coklat bubuk. Aduk-aduk bahan ini secara merata dan ayak dengan menggunakan saringan. Sisihkan sementara.</li>
          <li>Setelah tu, kita akan buat adonan basah untuk kue. Caranya, siapkan wadah atau baskom yang baru dan kocok telur bersama dengan emulsifier sampai mengembang dan mengental selama kurang lebih 10-15 menit. Gunakan kecepatan paling tinggi pada mixer dan kocok terus.</li>
          <li>Masukkan campuran tepung terigu dan coklat bubuk yang telah diayak, kemudian masukkan mentega yang telah dicairkan. Aduk-aduk bahan ini secara merata sampai tercampur merata.</li>
          <li>Nah, setelah semua bahan dicampur secara merata, silahkan bagi adonan kedalam 3 buah loyang yang sudah dipersiapkan dan ratakan bagian atasnya dengan menggunakan pisau.</li>
          <li>Masukkan adonan dalam loyang kedalam oven dengan suhu 180 derajat dan biarkan selama kurang lebih 25 menit. Angkat kue dan dinginkan.</li>
          <li>Basahi cherry hitam dan letakan satu lapisan kue, kemudian olesi dengan butter cream dan tempatkan cherry dibagian atasnya. Lakukan hal yang sama sampai lapisan kue terususun dengan cantik.</li>
